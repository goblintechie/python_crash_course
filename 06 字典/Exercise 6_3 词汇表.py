# Python字典可用于模拟现实生活中的字典。
# 为避免混淆，我们将后者称为词汇表。
# 想出你在前面学过的5个编程术语，将其用作词汇表中的键，并将它们的含义作为值存储在词汇表中。
# 以整洁的方式打印每个术语及其含义。
# 为此，可先打印术语，在它后面加上一个冒号，再打印其含义；
# 也可在一行打印术语，再使用换行符(\n)插入一个空行，然后在下一行以缩进的方式打印其含义。

programming_terms = {
    "变量": "用于存储数据的名称，可以随程序运行而改变其值。",
    "函数": "一段可以重复使用的代码，通过调用函数名来执行特定的任务。",
    "循环": "用于重复执行一段代码，直到满足某个条件为止。",
    "列表": "一种数据结构，可以存储一组有序的元素。",
    "字典": "一种数据结构，以键-值对的形式存储数据，方便快速查找和访问。"
}

for term, meaning in programming_terms.items():
    print(f"{term}: {meaning}\n")
