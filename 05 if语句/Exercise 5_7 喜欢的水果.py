# 喜欢的水果　创建一个列表，其中包含你喜欢的水果，再编写一系列独立的if语句，检查列表中是否包含特定的水果。
# 将该列表命名为favorite_fruits，并在其中包含三种水果。
# 编写5条if语句，每条都检查某种水果是否包含在列表中。
# 如果是，就打印一条消息，下面是一个例子。
# You really like bananas!

favorite_fruits = ["Apple", "Banana", "Orange", "Grape", "Strawberry", "Pineapple", "Mango", "Watermelon", "Cherry", "Peach"]

# more_fruits = ["Kiwi", "Plum", "Lemon", "Blueberry", "Avocado"]

if "Kiwi" in favorite_fruits:
    print("You really like Kiwi!")
if "Apple" in favorite_fruits:
    print("You really like Apple!")
if "Cherry" in favorite_fruits:
    print("You really like Cherry!")
if "Banana" in favorite_fruits:
    print("You really like Banana!")
if "Blueberry" in favorite_fruits:
    print("You really like Blueberry!")
